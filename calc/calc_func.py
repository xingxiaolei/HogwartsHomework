class Calculator:
    '''
    被测类 计算器
    '''
    def add(self, a, b):
        '''加法'''
        return a + b

    def sub(self, a, b):
        '''减法'''
        return a - b

    def mul(self, a, b):
        '''乘法'''
        return a * b

    def div(self, a, b):
        '''除法'''
        return a / b